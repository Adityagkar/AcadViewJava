// pattern print program

public class Class3_Pattern_AdityaGkar {
    
    public static void main(String[] args){
        int i=5,j=1;
        
        for(i=5;i>0;i--)
        {j=1;
            while(j<=i){
                System.out.print("*");
                j++;
            }
            System.out.println(" ");
        }
    }
    
}
